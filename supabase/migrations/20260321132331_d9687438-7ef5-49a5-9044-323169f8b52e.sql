<h2>O que é o Acordo de Sócios</h2>
<p>O Acordo de Sócios (também chamado de Acordo de Quotistas ou Shareholders'' Agreement) é um contrato parassocial que regula questões não abordadas pelo Contrato Social. Enquanto este último cuida da estrutura formal da sociedade, o Acordo de Sócios trata das regras do jogo entre os sócios.</p>

<h2>Cláusulas fundamentais</h2>
<ul>
<li><strong>Vesting</strong> — aquisição gradual de participação societária vinculada à permanência e performance, essencial para startups e sociedades com sócios operacionais</li>
<li><strong>Tag Along / Drag Along</strong> — proteção minoritária na venda de cotas e direito de arraste para viabilizar saídas</li>
<li><strong>Direito de preferência</strong> — prioridade dos sócios existentes na aquisição de cotas antes de terceiros</li>
<li><strong>Cláusula de deadlock</strong> — mecanismo de resolução de impasses decisórios (Russian Roulette, Texas Shoot-Out)</li>
<li><strong>Non-compete</strong> — vedação de atividade concorrente durante e após a sociedade</li>
<li><strong>Distribuição de lucros</strong> — política de reinvestimento vs. distribuição, percentuais mínimos e periodicidade</li>
</ul>

