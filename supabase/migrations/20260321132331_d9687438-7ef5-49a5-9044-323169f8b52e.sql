<h2>Distrato vs. Rescisão: qual a diferença?</h2>
<p>O <strong>distrato</strong> é a extinção do contrato por mútuo acordo das partes (Art. 472 do Código Civil). Exige a mesma forma do contrato original e pode prever condições específicas de transição. Já a <strong>rescisão unilateral</strong> ocorre quando uma das partes encerra o contrato sem a concordância da outra, podendo gerar penalidades.</p>

<h2>Hipóteses de rescisão sem multa</h2>
<p>O Código Civil prevê situações em que a rescisão é legítima sem pagamento de penalidades:</p>
<ul>
<li><strong>Descumprimento contratual</strong> (Art. 475 do CC) — a parte que descumpriu não pode exigir multa da parte prejudicada</li>
<li><strong>Exceção do contrato não cumprido</strong> (Art. 476 do CC) — se a outra parte não cumpriu sua obrigação, você pode suspender a sua</li>
<li><strong>Onerosidade excessiva</strong> (Art. 478 do CC) — quando evento extraordinário torna a prestação excessivamente onerosa</li>
<li><strong>Caso fortuito ou força maior</strong> (Art. 393 do CC) — eventos imprevisíveis e inevitáveis que impossibilitam o cumprimento</li>
<li><strong>Vícios de consentimento</strong> — erro, dolo, coação ou lesão na formação do contrato</li>
</ul>

<h2>Limites das multas contratuais</h2>
<p>A cláusula penal não pode exceder o valor da obrigação principal (Art. 412 do CC). Além disso, o juiz pode reduzir a penalidade quando for manifestamente excessiva ou quando a obrigação tiver sido parcialmente cumprida (Art. 413 do CC). Multas superiores a 10% do valor do contrato frequentemente são questionadas judicialmente.</p>

