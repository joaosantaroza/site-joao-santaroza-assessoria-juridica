<p>Antes de qualquer rescisão, envie notificação extrajudicial à outra parte. Esse documento constitui o devedor em mora, documenta os fundamentos da rescisão e demonstra boa-fé. A notificação deve conter: identificação do contrato, descrição do descumprimento, prazo para regularização e consequências do não atendimento.</p>

